const theme = createTheme();

export default function SignIn() {
  const dispatch =  useDispatch()
  const navigate = useNavigate()
  const [profile,setProfile] = useState({});
  const handleSubmit = (event) => {
    event.preventDefault();
    const data = new FormData(event.currentTarget);
    const userdata = {
      email: data.get('email'),
      password: data.get('password'),
    };
    axios.post("http://localhost:5000/api/user/login",userdata)
    .then((res)=>{console.log(res.data)
      // dispatch(login({isAuthenticated:true,token:res.data.token}));
      fetch("http://localhost:5000/api/user/loggeduser",{
                method:"GET",
                headers:{
                    "Content-Type":"application/json",
                    Authorization: `Bearer ${res.data.token}`
                }                    
            })
            .then((res)=>res.json())
            .then((res)=>{setProfile(res.user)
            console.log(res.user.name)
            dispatch(login({isAuthenticated:true,username:res.user.name}))
            // console.log(profile);
             navigate("/");

            })
            .catch((err)=>console.log(err));
      
    })
    .catch((err)=>console.log(err));
  };
  return (
    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            {/* <LockOutlinedIcon /> */}
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="#" variant="body2">
                  Forgot password?
                </Link>
              </Grid>
              <Grid item>
                <Link href="#" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}
